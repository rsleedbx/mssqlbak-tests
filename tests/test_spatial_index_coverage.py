"""Gap E-4 — Spatial index internal tessellation table.

A SPATIAL INDEX creates an internal tessellation B-tree (sysschobjs.type = 'IT').
recover_schema must skip it and return only the user geometry table.
read_table_rows must return the original geometry rows without crash.

Fixture: ``spatial_index_full.bak``
Generated by: ``python -m tools.fixture_run all-versions --suite spatial-index``
"""
from mssqlbak.catalog import recover_schema
from mssqlbak.pages import PageStore
from mssqlbak.rows import read_table_rows


def test_spatial_index_no_internal_tables(fixture_bak_spatial_index: str) -> None:
    """recover_schema must not expose internal spatial tessellation tables."""
    store = PageStore.from_bak(fixture_bak_spatial_index)
    schema = recover_schema(store)

    # Internal spatial index tables have names like sys_spatial_index_tessellation_...
    for tbl in schema.tables:
        assert not tbl.name.startswith("sys_spatial_index"), (
            f"Internal spatial tessellation table {tbl.name!r} must not be in schema"
        )


