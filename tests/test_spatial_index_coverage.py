def test_spatial_index_values(fixture_bak_spatial_index: str) -> None:
    """name column follows 'pt_N' pattern; loc column is not None."""
    from tools.make_spatial_index_fixture import TABLE

    store = PageStore.from_bak(fixture_bak_spatial_index)
    schema = recover_schema(store)
    tbl = next((t for t in schema.tables if t.name == TABLE), None)
    assert tbl is not None

    rows_by_id = {r["id"]: r for r in read_table_rows(store, tbl)}

    for sample_id in [1, 50, 100, 150, 200]:
        row = rows_by_id.get(sample_id)
        assert row is not None, f"row id={sample_id} missing"
        assert row["name"] == f"pt_{sample_id}", f"id={sample_id}: {row['name']!r}"
        # loc is a geometry value — at minimum it must not be None
        assert row["loc"] is not None, f"id={sample_id}: loc is None"
