"""Gap E-3 — XML index internal node table.

A PRIMARY XML INDEX creates an internal node table (sysschobjs.type = 'IT').
recover_schema must skip it and return only the user table.
read_table_rows must return the original XML rows without crash.

Fixture: ``xml_index_full.bak``
Generated by: ``python -m tools.fixture_run all-versions --suite xml-index``
"""
from mssqlbak.catalog import recover_schema
from mssqlbak.pages import PageStore
from mssqlbak.rows import read_table_rows


def test_xml_index_no_internal_tables(fixture_bak_xml_index: str) -> None:
    """recover_schema must not expose internal XML node tables as user tables."""
    store = PageStore.from_bak(fixture_bak_xml_index)
    schema = recover_schema(store)

    # All returned tables should be named by their user-table names.
    # Internal XML index node tables have system-generated names like
    # xml_index_nodes_XXXXXX_XXXXXXXX — they must not appear here.
    for tbl in schema.tables:
        assert not tbl.name.startswith("xml_index_nodes_"), (
            f"Internal XML node table {tbl.name!r} must not be in schema"
        )


