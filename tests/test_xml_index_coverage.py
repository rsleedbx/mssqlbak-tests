def test_xml_index_values(fixture_bak_xml_index: str) -> None:
    """Spot-check XML content and tag column values."""
    from tools.make_xml_index_fixture import TABLE

    store = PageStore.from_bak(fixture_bak_xml_index)
    schema = recover_schema(store)
    tbl = next((t for t in schema.tables if t.name == TABLE), None)
    assert tbl is not None

    rows_by_id = {r["id"]: r for r in read_table_rows(store, tbl)}

    for sample_id in [1, 25, 50, 99, 100]:
        row = rows_by_id.get(sample_id)
        assert row is not None, f"row id={sample_id} missing"
        # tag cycles through 'tag_0'..'tag_4'
        expected_tag = f"tag_{sample_id % 5}"
        assert row["tag"] == expected_tag, f"id={sample_id}: {row['tag']!r}"
        # doc column is XML — must not be None and must mention the id
        doc = row.get("doc")
        assert doc is not None, f"id={sample_id}: doc is None"
        doc_str = doc if isinstance(doc, str) else str(doc)
        assert str(sample_id) in doc_str, f"id={sample_id}: id missing from doc {doc_str!r}"
