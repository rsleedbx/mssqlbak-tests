@pytest.mark.fixture
def test_sql_variant_datetime2_value(fixture_bak_sql_variant_extract: Path) -> None:
    """id=5: CAST('2025-01-01' AS DATETIME2(0)) must decode to datetime(2025,1,1)."""
    rows = _read_sv(fixture_bak_sql_variant_extract)
    by_id = {r["id"]: r for r in rows}
    assert 5 in by_id, "id=5 missing"
    val = by_id[5]["val"]
