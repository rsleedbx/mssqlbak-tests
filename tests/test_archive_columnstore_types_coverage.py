# ---------------------------------------------------------------------------
# Cross-type consistency — all tables should agree on total + NULL counts
# ---------------------------------------------------------------------------

def test_all_types_agree_on_null_count(
    fixture_bak_types: Path,
) -> None:
    """All seven type tables produce the same NULL count.

    If any type's pool decoder is broken, its NULL count will diverge from
    the others.  This test surfaces that divergence in one place.
    """
    counts: dict[str, int] = {}
    for table, canonical, _ in _ALL_TABLES:
        rows = _rows(fixture_bak_types, table)
        counts[f"{table} ({canonical})"] = _null_count(rows, "val")

    wrong = {k: v for k, v in counts.items() if v != _NULL_COUNT}
    assert not wrong, (
        f"NULL count mismatch — expected {_NULL_COUNT} for all types:\n"
        + "\n".join(f"  {k}: {v}" for k, v in wrong.items())
    )
