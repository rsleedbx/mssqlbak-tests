"""Gap D-5 — VECTOR column type (SS2025 only).

The VECTOR(N) type (SS2025) stores a fixed-dimension float32 array in a binary
format: 4-byte header (type=0x53, dimensions uint16-LE, element_type=0x03)
followed by the packed float32 array.  mssqlbak must extract these without
crashing and return the correct float values (or raw bytes).

Fixture: ``vector_full.bak`` (SS2025 fixture directory only)
Generated by: ``python -m tools.fixture_run all-versions --suite vector --version 2025``
"""
import struct

import pytest

from mssqlbak.catalog import recover_schema
from mssqlbak.pages import PageStore
from mssqlbak.rows import read_table_rows


def _decode_vector(raw: object) -> list[float] | None:
    """Decode a VECTOR value returned by mssqlbak.

    mssqlbak surfaces VECTOR columns as raw bytes (VARBINARY on-disk).
    The SS2025 binary VECTOR format is an 8-byte header followed by packed
    float32 values:

      Bytes 0-1: format identifier LE uint16 (0x01A9 for float32 vectors)
      Bytes 2-3: number of dimensions LE uint16
      Bytes 4-7: reserved/flags LE uint32 (always 0)
      Bytes 8+:  float32 array, one element per dimension, LE byte order

    Returns a list of floats, or None if the value cannot be decoded.
    """
    if raw is None:
        return None
    if isinstance(raw, list):
        return [float(x) for x in raw]
    if isinstance(raw, str):
        stripped = raw.strip().lstrip("[").rstrip("]")
        return [float(x.strip()) for x in stripped.split(",")]
    if isinstance(raw, (bytes, bytearray)):
        # Need at least the 8-byte header
        if len(raw) < 8:
            return None
        n_dims = struct.unpack_from("<H", raw, 2)[0]
        floats_start = 8
        floats_end = floats_start + n_dims * 4
        if len(raw) < floats_end:
            return None
        return list(struct.unpack_from(f"<{n_dims}f", raw, floats_start))
    return None


