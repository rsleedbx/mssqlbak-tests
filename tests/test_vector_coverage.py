def test_vector_values_no_crash(fixture_bak_vector: str) -> None:
    """All VECTOR column values must be decodable (not None, not garbage)."""
    from tools.make_vector_fixture import TABLE

    store = PageStore.from_bak(fixture_bak_vector)
    schema = recover_schema(store)
    tbl = next((t for t in schema.tables if t.name == TABLE), None)
    assert tbl is not None

    for row in read_table_rows(store, tbl):
        raw = row.get("v")
        floats = _decode_vector(raw)
        assert floats is not None, f"id={row['id']}: cannot decode VECTOR value {raw!r}"
        assert len(floats) == 3, f"id={row['id']}: expected 3 floats, got {len(floats)}"


def test_vector_known_values(fixture_bak_vector: str) -> None:
    """Spot-check known float values for the first 5 rows."""
    from tools.make_vector_fixture import KNOWN_VECTORS, TABLE

    store = PageStore.from_bak(fixture_bak_vector)
    schema = recover_schema(store)
    tbl = next((t for t in schema.tables if t.name == TABLE), None)
    assert tbl is not None

    rows_by_id = {r["id"]: r for r in read_table_rows(store, tbl)}

    for row_id, expected_floats in KNOWN_VECTORS[:5]:
        row = rows_by_id.get(row_id)
        assert row is not None, f"row id={row_id} missing"
        actual = _decode_vector(row["v"])
        assert actual is not None, f"id={row_id}: cannot decode {row['v']!r}"
        assert len(actual) == len(expected_floats)
        for i, (a, e) in enumerate(zip(actual, expected_floats)):
            assert abs(a - e) < abs(e) * 1e-5 + 1e-6, (
                f"id={row_id}[{i}]: expected {e}, got {a}"
            )
